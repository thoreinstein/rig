func TestSecret_GetSecrets(t *testing.T) {
	tests := []struct {
		name       string
		keys       []string
		serverResp *apiv1.GetSecretsResponse
		want       map[string]string
	}{
		{
			name: "all_keys_found",
			keys: []string{"a", "b"},
			serverResp: &apiv1.GetSecretsResponse{
				Secrets: map[string]*apiv1.SecretValue{
					"a": {Value: "val-a"},
					"b": {Value: "val-b"},
				},
			},
			want: map[string]string{"a": "val-a", "b": "val-b"},
		},
		{
			name: "partial_keys_omitted",
			keys: []string{"a", "missing"},
			serverResp: &apiv1.GetSecretsResponse{
				Secrets: map[string]*apiv1.SecretValue{
					"a": {Value: "val-a"},
				},
			},
			want: map[string]string{"a": "val-a"},
		},
		{
			name: "nil_value_in_map_returns_empty_string",
			keys: []string{"a"},
			serverResp: &apiv1.GetSecretsResponse{
				Secrets: map[string]*apiv1.SecretValue{
					"a": nil,
				},
			},
			// Proto3 deserializes nil map values as empty messages, so the
			// client sees a non-nil *SecretValue with Value="".
			want: map[string]string{"a": ""},
		},
		{
			name: "empty_response",
			keys: []string{"a"},
			serverResp: &apiv1.GetSecretsResponse{
				Secrets: map[string]*apiv1.SecretValue{},
			},
			want: map[string]string{},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			stub := &stubSecretServer{
				getSecretsFunc: func(_ context.Context, _ *apiv1.GetSecretsRequest) (*apiv1.GetSecretsResponse, error) {
					return tt.serverResp, nil
				},
			}
			sock := startStubServer(t, stub)

			s := NewSecret(WithSecretHostEndpoint(sock))
			defer s.Close()

			got, err := s.GetSecrets(t.Context(), tt.keys)
			require.NoError(t, err)
			require.Equal(t, tt.want, got)
		})
	}
}

func TestSecret_GetSecret_unauthenticated_error(t *testing.T) {
	stub := &stubSecretServer{
		getSecretFunc: func(_ context.Context, _ *apiv1.GetSecretRequest) (*apiv1.GetSecretResponse, error) {
			return nil, status.Errorf(codes.Unauthenticated, "missing plugin identity")
		},
	}
	sock := startStubServer(t, stub)

	s := NewSecret(WithSecretHostEndpoint(sock))
	defer s.Close()

	_, err := s.GetSecret(t.Context(), "key")
	require.Error(t, err)

	st, ok := status.FromError(err)
	require.True(t, ok, "error should be a gRPC status")
	require.Equal(t, codes.Unauthenticated, st.Code())
}
